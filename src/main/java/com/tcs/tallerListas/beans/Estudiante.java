package com.tcs.tallerListas.beans;

public class Estudiante {

	private String idestudiante;
	private String nombreestudiante;
	private int edadestudiante;
	private String gradoestudiante;
	private String reprobaron;

	public Estudiante() {

	}

	public String getIdestudiante() {
		return idestudiante;
	}

	public void setIdestudiante(String idestudiante) {
		this.idestudiante = idestudiante;
	}

	public String getNombreestudiante() {
		return nombreestudiante;
	}

	public void setNombreestudiante(String nombreestudiante) {
		this.nombreestudiante = nombreestudiante;
	}

	public int getEdadestudiante() {
		return edadestudiante;
	}

	public void setEdadestudiante(int edadestudiante) {
		this.edadestudiante = edadestudiante;
	}

	public String getGradoestudiante() {
		return gradoestudiante;
	}

	public void setGradoestudiante(String gradoestudiante) {
		this.gradoestudiante = gradoestudiante;
	}

	public String getReprobaron() {
		return reprobaron;
	}

	public void setReprobaron(String reprobaron) {
		this.reprobaron = reprobaron;
	}

}
